
//suma
function suma (n1,n2){
    var s = n1 + n2
    return s
}
//resta
function rest (n1,n2){
    var r = n1 - n2
    return r
}
//multiplicacion
function mul(n1,n2){
    let m=n1*n2
    return m
}
//divicion
function div(n1,n2){
    let d=n1/n2
    return d
}

console.log(suma(9,7));
console.log(rest(9,7));
console.log(mul(1800,5));
console.log(div(9000,5));
    